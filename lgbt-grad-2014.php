                  For Shen Heckel, abandoning his faith was never an option. It was his emerging sexuality that he wanted to run away from.

                  "I've always been very Catholic and I was struggling (because) if this does make me gay, do I belong in the church?" said Heckel, a UCLA alumnus who identifies as Catholic and a transgender man, about first realizing his sexuality.

                  Despite Heckel's perceived polarity of these two communities, he said he eventually found peace by connecting with someone toward the end of his college career who was also going through a similar experience.

                  "I kept praying and hoping to find someone that I could relate to... and I ended up finding someone that was transgender and Catholic," Heckel said. "I think he really helped me through (coming out)... we were able to talk about both worlds and have emotional connections to both worlds. It made me feel like I am not alone."

